namespace _2D_RPG_Negiramen.Models.FileEntries
{
    using Tomlyn;
    using Tomlyn.Model;
    using TheLocationOfUnityAssets = _2D_RPG_Negiramen.Models.FileEntries.Locations.UnityAssets;

    /// <summary>
    ///     😁 構成
    /// </summary>
    class UserConfiguration
    {
        // - 静的プロパティー

        internal static string AutoGenerated { get; } = "Auto Generated";

        // - 静的メソッド

        /// <summary>
        ///     <pre>
        ///         TOML形式ファイルの読取
        ///     
        ///         📖　[Tomlyn　＞　Documentation](https://github.com/xoofx/Tomlyn/blob/main/doc/readme.md)
        ///     </pre>
        /// </summary>
        /// <param name="configuration">構成</param>
        /// <returns>TOMLテーブルまたはヌル</returns>
        internal static bool LoadTOML(out UserConfiguration configuration)
        {
            try
            {
                // ユーザー構成ファイルへのパスは構成ファイルに与えられているので、これを使う
                var userConfigurationFilePath = App.GetOrLoadConfiguration().UserConfigurationFile.Path.AsStr;
                // Example: `"C:/Users/むずでょ/Documents/GitHub/2D-RPG-Negiramen/Workspace/user_configuration.toml"`

                // 設定ファイルの読取
                var userConfigurationText = System.IO.File.ReadAllText(userConfigurationFilePath);


                Locations.Negiramen.WorkingTilesetImageFile workingTilesetImageFile = new Models.FileEntries.Locations.Negiramen.WorkingTilesetImageFile();

                TheLocationOfUnityAssets.ItsFolder unityAssetsFolder = new TheLocationOfUnityAssets.ItsFolder();

                Locations.Negiramen.UserConfigurationFile userConfiguration = new Models.FileEntries.Locations.Negiramen.UserConfigurationFile();
                YourCircleName yourCircleName = new YourCircleName();
                YourWorkName yourWorkName = new YourWorkName();

                // TOML
                TomlTable document = Toml.ToModel(userConfigurationText);

                if (document != null)
                {
                    //
                    // [paths]
                    // =======
                    //
                    if (document.TryGetValue("paths", out object pathsObj))
                    {
                        if (pathsObj != null && pathsObj is TomlTable paths)
                        {
                            // ネギラーメン・ワークスペースの作業中のタイルセット画像ファイルへのパス
                            if (paths.TryGetValue("working_tileset", out object workingTilesetFilePathObj))
                            {
                                if (workingTilesetFilePathObj is string workingTilesetFilePathAsStr)
                                {
                                    workingTilesetImageFile = new Locations.Negiramen.WorkingTilesetImageFile(
                                        pathSource: FileEntryPathSource.FromString(workingTilesetFilePathAsStr),
                                        convert: (pathSource) => FileEntryPath.From(pathSource,
                                                                                    replaceSeparators: true,
                                                                                    // 変数展開に備える
                                                                                    expandVariables: App.GetOrLoadConfiguration().Variables));
                                }
                            }
                        }
                    }
                }

                configuration = new UserConfiguration(
                    workingTilesetImageFile);
                return true;
            }
            catch (Exception ex)
            {
                // TODO 例外対応、何したらいい（＾～＾）？
                configuration = null;
                return false;
            }
        }

        /// <summary>
        ///     保存
        /// </summary>
        /// <param name="current">現在の構成</param>
        /// <param name="difference">現在の構成から更新した差分</param>
        /// <param name="newConfiguration">差分を反映した構成</param>
        /// <returns>完了した</returns>
        internal static bool SaveTOML(UserConfiguration current, UserConfigurationBuffer difference, out UserConfiguration newConfiguration)
        {
            var configurationBuffer = new UserConfigurationBuffer();

            // 差分適用
            configurationBuffer.WorkingTilesetImageFile = difference.WorkingTilesetImageFile == null ? current.WorkingTilesetImageFile : difference.WorkingTilesetImageFile;

            //
            // 注意：　変数展開後のパスではなく、変数展開前のパス文字列を保存すること
            //
            var text = $@"[paths]

# ネギラーメン・ワークスペースの作業中のタイルセット画像ファイルへのパス
working_tileset = ""{configurationBuffer.WorkingTilesetImageFile.PathSource.AsStr}""
";

            // 上書き
            System.IO.File.WriteAllText(
                // 保存したいファイルへのパス
                path: App.GetOrLoadConfiguration().UserConfigurationFile.Path.AsStr,
                contents: text);

            // イミュータブル・オブジェクトを生成
            newConfiguration = new UserConfiguration(
                configurationBuffer.WorkingTilesetImageFile);
            return true;
        }

        // - インターナル・プロパティー

        #region プロパティ（ネギラーメン・ワークスペースの作業中のタイルセット画像ファイルへのパス）
        /// <summary>
        ///     ネギラーメン・ワークスペースの作業中のタイルセット画像ファイルへのパス
        /// </summary>
        /// <example>"C:/Users/むずでょ/Documents/GitHub/2D-RPG-Negiramen/Workspace/Temporary/Images/working_tileset.png"</example>
        internal Locations.Negiramen.WorkingTilesetImageFile WorkingTilesetImageFile { get; }
        #endregion

        // - その他

        /// <summary>
        ///     生成
        /// </summary>
        internal UserConfiguration() : this(
            Locations.Negiramen.WorkingTilesetImageFile.Empty)
        {
        }

        /// <summary>
        ///     生成
        /// </summary>
        /// <param name="negiramenWorkspaceFolderPath">ネギラーメン・ワークスペースの作業中のタイルセット画像ファイルへのパス</param>
        internal UserConfiguration(
            Locations.Negiramen.WorkingTilesetImageFile workingTilesetImageFile)
        {
            this.WorkingTilesetImageFile = workingTilesetImageFile;
        }
    }
}
